async function uploadArticles() {
  let successCount = 0;
  let failCount = 0;

  console.log(`Starting upload of ${articles.length} articles via API...\n`);

  for (const article of articles) {
    try {
      console.log(`Uploading: ${article.title}`);
      
      const response = await fetch('http://localhost:5200/api/admin/blog', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          ...article,
          image: article.imageUrl,
          description: article.excerpt,
        }),
      });

      if (response.ok) {
        console.log(`✓ Successfully uploaded: ${article.slug}\n`);
        successCount++;
      } else {
        const error = await response.text();
        console.log(`✗ Failed to upload ${article.slug}: ${error}\n`);
        failCount++;
      }

      // Small delay between uploads
      await new Promise(resolve => setTimeout(resolve, 300));
    } catch (error) {
      console.log(`✗ Error uploading ${article.slug}:`, error);
      failCount++;
    }
  }

  console.log(`\n========================================`);
  console.log(`Upload complete!`);
  console.log(`Success: ${successCount}/${articles.length}`);
  console.log(`Failed: ${failCount}/${articles.length}`);
  console.log(`========================================\n`);
}

uploadArticles();
